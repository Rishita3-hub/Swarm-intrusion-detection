        'yo','MarkerFaceColor','y','MarkerSize',4);

    plot3(x,-7,0.30,...
        'yo','MarkerFaceColor','y','MarkerSize',4);
end

%% ===============================================================
% TAXIWAYS
%% ===============================================================
fill3([-32 -15 -15 -32],...
      [10 10 24 24],...
      [0 0 0 0],...
      [0.28 0.28 0.28]);

fill3([15 32 32 15],...
      [-10 -10 -24 -24],...
      [0 0 0 0],...
      [0.28 0.28 0.28]);

%% ===============================================================
% BUILDINGS
%% ===============================================================
